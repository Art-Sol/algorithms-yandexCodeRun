function getMaxGrade(N, staff, K) {
  let sum = 0;

  if (N === 0) {
    return sum;
  }

  if (K === N) {
    for (let i = 0; i < N; i++) {
      sum += staff[i];
    }

    return sum;
  }

  let usedKeyboards = 0;
  const MAX_GRADE = 25;
  const countArr = Array(MAX_GRADE + 1).fill(0);

  staff.forEach((grade) => countArr[grade]++);

  for (let i = countArr.length - 1; i >= 0; i--) {
    if (usedKeyboards >= K) {
      break;
    }

    if (countArr[i] === 0) {
      continue;
    }

    for (let j = 0; j < countArr[i]; j++) {
      if (usedKeyboards < K) {
        sum += i;
        usedKeyboards++;
      } else {
        break;
      }
    }
  }

  return sum;
}

